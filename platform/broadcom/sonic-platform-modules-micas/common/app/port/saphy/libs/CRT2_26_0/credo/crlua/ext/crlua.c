/*
** Main body of stand-alone interpreter (to be called in protected mode).
** Reads the options and handles them all.
*/
static int pmain(lua_State *L) {
    int argc = (int)lua_tointeger(L, 1);
    char **argv = (char **)lua_touserdata(L, 2);
    int script;
    int args = collectargs(argv, &script);
    luaL_checkversion(L); /* check that interpreter has correct version */
    if (args == has_h) {
        print_usage(L, NULL);
        lua_pushboolean(L, 1); /* signal no errors */
        return 1;
    }
    if (args == has_error) {          /* bad arg? */
        print_usage(L, argv[script]); /* 'script' has index of bad arg. */
        return 0;
    }
    if (args & has_v) /* option '-v'? */
        print_version(L);
    if (args & has_E) {        /* option '-E'? */
        lua_pushboolean(L, 1); /* signal for libraries to ignore env. vars. */
        lua_setfield(L, LUA_REGISTRYINDEX, "LUA_NOENV");
    }
    luaL_openlibs(L);                      /* open standard libraries */
    crluaL_openlibs(L);                    /* open custom libraries */
    createargtable(L, argv, argc, script); /* create table 'arg' */
    lua_gc(L, LUA_GCGEN, 0, 0);            /* GC in generational mode */
    if (!(args & has_E)) {                 /* no option '-E'? */
        if (handle_luainit(L) != LUA_OK)   /* run LUA_INIT */
            return 0;                      /* error running LUA_INIT */
    }
    if (!runargs(L, argv, script)) /* execute arguments -e and -l */
        return 0;                  /* something failed */
    if (script < argc &&           /* execute main script (if there is one) */
        handle_script(L, argv + script) != LUA_OK)
        return 0;
    if (args & has_i)                                       /* -i option? */
        doREPL(L);                                          /* do read-eval-print loop */
    else if (script == argc && !(args & (has_e | has_v))) { /* no arguments? */
        print_version(L);
        print_crlua_banner(L);
        doREPL(L); /* do read-eval-print loop */
    }
    lua_pushboolean(L, 1); /* signal no errors */
    return 1;
}

// add in custom bindings for printing

size_t lua_writestring(lua_State *L, const char *str, int length) {
    if (L != NULL) {
        lua_pushstring(L, "credo_writer");
        lua_gettable(L, LUA_REGISTRYINDEX);
        CredoShellWriter_t writer = lua_touserdata(L, -1);
        lua_pop(L, 1);

        lua_pushstring(L, "credo_ud");
        lua_gettable(L, LUA_REGISTRYINDEX);
        void *ud = lua_touserdata(L, -1);
        lua_pop(L, 1);

        if (writer != NULL) {
            writer(ud, CR_SHELL_STDOUT, str);
            return length;
        }
    }
    if (crsh.writer != NULL) {
        crsh.writer(crsh.writer_ud, CR_SHELL_STDOUT, str);
    } else {
        fwrite(str, sizeof(char), length, stdout);
    }
    return length;
}

int lua_writestringerror(lua_State *L, const char *format, const char *p) {
    char buff[1024];

    if (L != NULL) {
        lua_pushstring(L, "credo_writer");
        lua_gettable(L, LUA_REGISTRYINDEX);
        CredoShellWriter_t writer = lua_touserdata(L, -1);
        lua_pop(L, 1);

        lua_pushstring(L, "credo_ud");
        lua_gettable(L, LUA_REGISTRYINDEX);
        void *ud = lua_touserdata(L, -1);
        lua_pop(L, 1);

        if (writer != NULL) {
            snprintf(buff, sizeof(buff), format, p);
            writer(ud, CR_SHELL_STDERR, buff);
            return 0;
        }
    }

    if (crsh.writer != NULL) {
        char *b = buff;
        char *mb = NULL;
        int len = snprintf(b, sizeof(buff), format, p);
        if ((size_t)len >= sizeof(buff)) {
            mb = malloc(len);
            if (mb != NULL) {
                snprintf(mb, len, format, p);
                b = mb;
            }
        }
        crsh.writer(crsh.writer_ud, CR_SHELL_STDERR, b);
        free(mb);
    } else {
        fprintf(stderr, format, p);
    }
    return 0;
}

static lua_State *CL = NULL;

void cr_lua_get_completions(const char *buf, void *lc, size_t pos) {
    if (CL == NULL) return;
    lua_pushstring(CL, CR_REGISTRY_COMPLETER);
    int ltype = lua_gettable(CL, LUA_REGISTRYINDEX);
    if (ltype != LUA_TFUNCTION) {
        lua_pop(CL, 1);
        return;
    }
    lua_pushstring(CL, buf);
    lua_pushlightuserdata(CL, lc);
    lua_pushinteger(CL, (lua_Integer)pos);
    int status = lua_pcall(CL, 3, 0, 0);
    report(CL, status);
}

static int crlua_panic(lua_State *L) {
    const char *msg = lua_tostring(L, -1);
    lua_pushliteral(L, "credo_jmpbuf");
    lua_gettable(L, LUA_REGISTRYINDEX);
    jmp_buf *buf = lua_touserdata(L, -1);
    if (msg == NULL) msg = "error object is not a string";
    lua_writestringerror(L, "PANIC: unprotected error in call to Lua API (%s)\n", msg);
    longjmp(*buf, 1);
    return 0;
}

static inline void lregister_cmd_slices(lua_State *L, CredoSlice_t **slices, size_t slice_count) {
    lua_newtable(L);
    lua_newtable(L);
    if (slices != NULL && *slices != NULL) {
        for (size_t i = 0; i < slice_count; i++) {
            // add slice list
            lua_pushinteger(L, (lua_Integer)i + 1);
            lua_pushinteger(L, slices[i]->slice_id);
            lua_settable(L, -4);
            // add slice map
            lua_pushinteger(L, slices[i]->slice_id);
            lua_pushlightuserdata(L, slices[i]);
            lua_settable(L, -3);
        }
    }
    lua_pushstring(L, CR_REGISTRY_CMD_SLICES);
    lua_pushvalue(L, -3);
    lua_settable(L, LUA_REGISTRYINDEX);

    lua_pushstring(L, CR_REGISTRY_CMD_SLICES_MAP);
    lua_pushvalue(L, -2);
    lua_settable(L, LUA_REGISTRYINDEX);
    lua_pop(L, 2);
}

static inline void lregister_panic(lua_State *L, jmp_buf *buf) {
    lua_pushliteral(L, "credo_jmpbuf");
    lua_pushlightuserdata(L, buf);
    lua_settable(L, LUA_REGISTRYINDEX);
    lua_atpanic(L, crlua_panic);
}

static inline void lregister_writer(lua_State *L, CredoShellWriter_t writer, void *ud) {
    if (writer == NULL) {
        return;
    }
    lua_pushliteral(L, "credo_writer");
    lua_pushlightuserdata(L, writer);
    lua_settable(L, LUA_REGISTRYINDEX);

    lua_pushliteral(L, "credo_ud");
    lua_pushlightuserdata(L, ud);
    lua_settable(L, LUA_REGISTRYINDEX);
}

CredoError_t cr_lua_exec(int argc, char *argv[]) {
    SHELL_LOCK();
    CredoError_t err = cr_lua_exec_full(argc, argv, crsh.slices, crsh.slice_count);
    SHELL_UNLOCK();
    return err;
}

CredoError_t cr_lua_exec_full(int argc, char *argv[], CredoSlice_t *slices[], size_t slice_count) {
    int status, result;
    lua_State *L = luaL_newstate(); /* create state */
    if (L == NULL) {
        l_message(NULL, argv[0], "cannot create state: not enough memory");
        return CR_FAIL;
    }
    lua_State *old_state = CL;
    CL = L;
    // prevent lua from aborting
    jmp_buf jbuf;
    lregister_panic(L, &jbuf);
    if (setjmp(jbuf) != 0) {
        status = LUA_ERRRUN;
        result = false;
        goto cleanup;
    }
    lregister_cmd_slices(L, slices, slice_count);
    lua_pushliteral(L, "credo_in_shell");
    lua_pushboolean(L, true);
    lua_settable(L, LUA_REGISTRYINDEX);

    lua_pushcfunction(L, &pmain);   /* to call 'pmain' in protected mode */
    lua_pushinteger(L, argc);       /* 1st argument */
    lua_pushlightuserdata(L, argv); /* 2nd argument */
    status = lua_pcall(L, 2, 1, 0); /* do the call */
    result = lua_toboolean(L, -1);  /* get result */
    report(L, status);
cleanup:
    lua_close(L);
    CL = old_state;
    clearerr(stdin);  // clear the EOF error to allow the user to re-enter
    return (result && status == LUA_OK) ? CR_OK : CR_FAIL;
}

static int prun_command(lua_State *L) {
    const char *command = luaL_checkstring(L, 1);
    luaL_openlibs(L);           /* open standard libraries */
    crluaL_openlibs(L);         /* open custom libraries */
    lua_gc(L, LUA_GCGEN, 0, 0); /* GC in generational mode */
    lua_getglobal(L, "slash");
    lua_pushstring(L, command);
    lua_call(L, 1, 0);
    return 0;
}

CredoError_t crlua_run_command(CredoSlice_t *slices[], size_t slice_count, const char *command,
                               CredoShellWriter_t writer, void *ud) {
    lua_State *L = luaL_newstate(); /* create state */
    if (L == NULL) {
        l_message(NULL, "lua", "cannot create state: not enough memory");
        return CR_FAIL;
    }
    // prevent lua from aborting
    jmp_buf jbuf;
    lregister_panic(L, &jbuf);
    if (setjmp(jbuf) != 0) {
        lua_close(L);
        return CR_FAIL;
    }
    lregister_cmd_slices(L, slices, slice_count);
    lregister_writer(L, writer, ud);
    lua_pushcfunction(L, &prun_command);
    lua_pushstring(L, command);
    int status = lua_pcall(L, 1, 0, 0); /* do the call */
    report(L, status);
    lua_close(L);
    return (status == LUA_OK) ? CR_OK : CR_FAIL;
}

