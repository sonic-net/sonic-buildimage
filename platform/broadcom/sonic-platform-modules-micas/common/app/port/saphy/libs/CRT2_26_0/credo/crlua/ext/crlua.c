#define LUA_PROMPT  "crlua > "
#define LUA_PROMPT2 "crlua >> "

#if !defined(LUA_MAXINPUT)
#define LUA_MAXINPUT 1024
#endif

static inline int lua_readline(lua_State *L, char *buffer, const char *prompt) {
    CredoReadLine_t func = crsh.readline;
    if (func != NULL) {
        return func(prompt, buffer) == CR_OK;
    }

    (void)(L);  // following readline api call, prevent unused
    fputs(prompt, stdout);
    fflush(stdout);
    return fgets(buffer, LUA_MAXINPUT, stdin) != NULL;
}
#define lua_initreadline(L) ((void)(L))
#define lua_saveline(L, line) \
    {                         \
        (void)(L);            \
        (void)(line);         \
    }
#define lua_freeline(L, b) \
    {                      \
        (void)(L);         \
        (void)(b);         \
    }

/*
** Return the string to be used as a prompt by the interpreter. Leave
** the string (or nil, if using the default value) on the stack, to keep
** it anchored.
*/
static const char *get_prompt(lua_State *L, int firstline) {
    if (lua_getglobal(L, firstline ? "_PROMPT" : "_PROMPT2") != LUA_TNIL) {
        /* apply 'tostring' over the value */
        const char *p = luaL_tolstring(L, -1, NULL);
        lua_remove(L, -2); /* remove original value */
        return p;
    }
    // try and use custom shell prompt function
    lua_pushstring(L, CR_REGISTRY_SHELL_PROMPT);
    lua_gettable(L, LUA_REGISTRYINDEX);
    lua_pushboolean(L, firstline);
    if (lua_pcall(L, 1, 1, 0) == 0) {
        const char *p = lua_tostring(L, -1);
        lua_remove(L, -2);
        return p;
    }
    lua_pushstring(L, (firstline ? LUA_PROMPT : LUA_PROMPT2));
    lua_remove(L, -2);
    lua_remove(L, -3);
    return firstline ? LUA_PROMPT : LUA_PROMPT2;
}

/* mark in error messages for incomplete statements */
#define EOFMARK "<eof>"
#define marklen (sizeof(EOFMARK) / sizeof(char) - 1)

/*
** Check whether 'status' signals a syntax error and the error
** message at the top of the stack ends with the above mark for
** incomplete statements.
*/
static int incomplete(lua_State *L, int status) {
    if (status == LUA_ERRSYNTAX) {
        size_t lmsg;
        const char *msg = lua_tolstring(L, -1, &lmsg);
        if (lmsg >= marklen && strcmp(msg + lmsg - marklen, EOFMARK) == 0) {
            lua_pop(L, 1);
            return 1;
        }
    }
    return 0; /* else... */
}

#define LUA_RESTART_STMT "Restarting the Shell"

/*
** Prompt the user, read a line, and push it into the Lua stack.
*/

static int pushline(lua_State *L, int firstline, bool *G) {
    char buffer[LUA_MAXINPUT];
    char *b = buffer;
    size_t l;
    const char *prmt = get_prompt(L, firstline);
    int readstatus = lua_readline(L, b, prmt);

    if (readstatus == 0) return 0; /* no input (prompt will be popped by caller) */
    lua_pop(L, 1);                 /* remove prompt */
    l = strlen(b);
    if (l > 0 && b[l - 1] == '\n')              /* line ends with newline? */
        b[--l] = '\0';                          /* remove it */
    if (firstline && b[0] == '=')               /* for compatibility with 5.2, ... */
        lua_pushfstring(L, "return %s", b + 1); /* change '=' to 'return' */
    else if ((firstline && (strcmp(b, "quit") == 0 || strcmp(b, "exit") == 0))) {
        return 0;  // user wants to exit
    } else if (strcmp(b, "%restart") == 0) {
        crsh.restart = true;
        lua_writestring(L, LUA_RESTART_STMT, strlen(LUA_RESTART_STMT));
        lua_writeline(L);
        return 0;
    } else {
        lua_pushstring(L, CR_REGISTRY_MUI);
        int ltype = lua_gettable(L, LUA_REGISTRYINDEX);
        if (ltype == LUA_TFUNCTION) {
            lua_pushboolean(L, firstline);
            lua_pushstring(L, b);
            int status = lua_pcall(L, 2, 2, 0);
            report(L, status);
            if (status != LUA_OK) {
                lua_pushliteral(L, "");
            }
            *G |= lua_toboolean(L, -1);
            lua_pop(L, 1);
        } else {
            lua_pop(L, 1);
            lua_pushstring(L, b);
            *G = false;
        }
    }
    lua_freeline(L, b);
    return 1;
}

/*
** Try to compile line on the stack as 'return <line>;'; on return, stack
** has either compiled chunk or original line (if compilation failed).
*/
static int addreturn(lua_State *L) {
    const char *line = lua_tostring(L, -1); /* original line */
    const char *retline = lua_pushfstring(L, "return %s;", line);
    int status = luaL_loadbuffer(L, retline, strlen(retline), "=stdin");
    if (status == LUA_OK) {
        lua_remove(L, -2);         /* remove modified line */
        if (line[0] != '\0')       /* non empty? */
            lua_saveline(L, line); /* keep history */
    } else
        lua_pop(L, 2); /* pop result from 'luaL_loadbuffer' and modified line */
    return status;
}

/*
** Read multiple lines until a complete Lua statement
*/
static int multiline(lua_State *L, bool *G) {
    for (;;) { /* repeat until gets a complete statement */
        size_t len;
        const char *line = lua_tolstring(L, 1, &len);         /* get what it has */
        int status = luaL_loadbuffer(L, line, len, "=stdin"); /* try it */
        if (!incomplete(L, status) || !pushline(L, 0, G)) {
            lua_saveline(L, line); /* keep history */
            return status;         /* cannot or should not try to add continuation line */
        }
        lua_pushliteral(L, "\n"); /* add newline... */
        lua_insert(L, -2);        /* ...between the two lines */
        lua_concat(L, 3);         /* join them */
    }
}

/*
** Read a line and try to load (compile) it first as an expression (by
** adding "return " in front of it) and second as a statement. Return
** the final status of load/call with the resulting function (if any)
** in the top of the stack.
*/
static int loadline(lua_State *L, bool *G) {
    int status;
    lua_settop(L, 0);
    if (!pushline(L, 1, G)) return -1;     /* no input */
    if ((status = addreturn(L)) != LUA_OK) /* 'return ...' did not work? */
        status = multiline(L, G);          /* try as command, maybe with continuation lines */
    lua_remove(L, 1);                      /* remove line from the stack */
    lua_assert(lua_gettop(L) == 1);
    return status;
}

/*
** Prints (calling the Lua 'print' function) any values on the stack
*/
static void l_print(lua_State *L) {
    int n = lua_gettop(L);
    if (n > 0) { /* any result to be printed? */
        luaL_checkstack(L, LUA_MINSTACK, "too many results to pprint");
        lua_getglobal(L, "pprint");
        lua_insert(L, 1);
        if (lua_pcall(L, n, 0, 0) != LUA_OK)
            l_message(L, progname, lua_pushfstring(L, "error calling 'pprint' (%s)", lua_tostring(L, -1)));
    }
}

/*
** Do the REPL: repeatedly read (load) a line, evaluate (call) it, and
** print any results.
*/

static void doREPL(lua_State *L) {
    int status;
    lua_initreadline(L);
    bool use_G = false;
    while ((status = loadline(L, &use_G)) != -1) {
        if (status == LUA_OK) status = docall(L, 0, LUA_MULTRET, use_G);
        if (status == LUA_OK)
            l_print(L);
        else
            report(L, status);
        use_G = false;
    }
    lua_settop(L, 0); /* clear stack */
    lua_writeline(L);
}

