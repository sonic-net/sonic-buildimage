/** Read a register from the currently selected Serdes IP Lane.
 * @param sa__ is an opaque state vector passed through to device access functions.
 * @param address Address of register to be read
 * @param *val value read out from the register
 * @return Error code generated by read function (returns ERR_CODE_NONE if no errors)
 */
err_code_t plp_barchetta_blackhawk_barchetta_pmd_rdt_reg(srds_access_t *sa__, uint16_t address, uint16_t *val)
{
    uint32_t data = 0, addr = 0;
    addr = (address) | (1 << 16);
    PHYMOD_IF_ERR_RETURN(
        _plp_barchetta_reg_read(sa__, addr, &data));
    *val = (uint16_t)data;
    return ( ERR_CODE_NONE );
}

/** Write to a register from the currently selected Serdes IP Lane.
 * @param sa__ is an opaque state vector passed through to device access functions.
 * @param address Address of register to be written
 * @param val Value to be written to the register
 * @return Error code generated by write function (returns ERR_CODE_NONE if no errors)
 */
err_code_t plp_barchetta_blackhawk_barchetta_pmd_wr_reg(srds_access_t *sa__, uint16_t address, uint16_t val)
{
    uint32_t data = 0xffff & val;
    uint32_t addr = 0;
    addr = (address) | (1 << 16);

    PHYMOD_IF_ERR_RETURN(
       _plp_barchetta_reg_write(sa__, addr, data));

    return (ERR_CODE_NONE);
}

/** Masked Register Write to the currently selected Serdes IP core/lane.
 * If using Serdes MDIO controller to access the registers, implement this function using blackhawk_barchetta_pmd_mdio_mwr_reg(..)
 *
 * If NOT using a Serdes MDIO controller or the Serdes PMI Masked write feature, please use the following code to
 * implement this function
 *
 *    plp_barchetta_blackhawk_barchetta_pmd_wr_reg(addr, ((blackhawk_barchetta_pmd_rd_reg(addr) & ~mask) | (mask & (val << lsb))));
 *
 * @param sa__ is an opaque state vector passed through to device access functions.
 * @param addr Address of register to be written
 * @param mask 16-bit mask indicating the position of the field with bits of 1s
 * @param lsb  LSB of the field
 * @param val  16bit value to be written
 * @return Error code generated by write function (returns ERR_CODE_NONE if no errors)
 */
err_code_t plp_barchetta_blackhawk_barchetta_pmd_mwr_reg(srds_access_t *sa__, uint16_t addr, uint16_t mask, uint8_t lsb, uint16_t val)
{
    uint16_t tmp, otmp;
    uint32_t reg_val;
    val = val << lsb;

    val = val & mask ;
    /* Use clause 45 access if supported */
    PHYMOD_IF_ERR_RETURN(
        _plp_barchetta_reg_read(sa__, (addr | (1 << 16)), &reg_val));
    tmp = (uint16_t) (reg_val & 0xffff);
    otmp = tmp;
    tmp &= ~(mask);
    tmp |= val;

    if (otmp != tmp) {
        PHYMOD_IF_ERR_RETURN(
            _plp_barchetta_reg_write(sa__, (addr | (1 << 16)), tmp));
    }

    return (ERR_CODE_NONE);
}

