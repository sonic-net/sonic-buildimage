/** Return the address of current selected Serdes IP lane.
 * @param sa__ is an opaque state vector passed through to device access functions.
 * @return the IP level address of the current lane. 0 to N-1, for an N lane IP
 */
uint8_t plp_barchetta_blackhawk_barchetta_get_lane(srds_access_t *sa__)
{
    int lane_map = 0, lane_idx=0;
    BCMI_BARCHETTA_SLICE_SLICE0r_t slice0;

    PHYMOD_IF_ERR_RETURN(
        BCMI_BARCHETTA_READ_SLICE_SLICE0r(sa__, &slice0));
	lane_map = BCMI_BARCHETTA_SLICE_SLICE0r_LANE_SELf_GET(slice0);
	for (lane_idx = 0; lane_idx < 8; lane_idx ++) {
	    if (lane_map & (1 << lane_idx)) {
			return lane_idx;
		}
	}
	/* default return 0*/
    return 0 ;


}

/** Set the address of current selected Serdes IP lane.  Used in diagnostic
 * and core-level management functions.
 * @param sa__ is an opaque state vector passed through to device access functions.
 * @param lane_index is the lane index
 * @return Any error code generated during execution; ERR_CODE NONE otherwise.
 */
err_code_t plp_barchetta_blackhawk_barchetta_set_lane(srds_access_t *sa__, uint8_t lane_index)
{
    BCMI_BARCHETTA_SLICE_SLICE0r_t slice0;
    PHYMOD_IF_ERR_RETURN(
        BCMI_BARCHETTA_READ_SLICE_SLICE0r(sa__, &slice0));
    BCMI_BARCHETTA_SLICE_SLICE0r_LANE_SELf_SET(slice0, (1 << lane_index));
    PHYMOD_IF_ERR_RETURN(
        BCMI_BARCHETTA_WRITE_SLICE_SLICE0r(sa__, slice0));
    return(ERR_CODE_NONE);
}


