/** Set the address of current selected Serdes IP PLL.  Used in disgnoztic
 * and core-level management functions.
 * @param sa__ is an opaque state vector passed through to device access functions.
 * @param pll_index is the PLL index
 * @return Any error code generated during execution; ERR_CODE NONE otherwise.
 */
err_code_t plp_barchetta_blackhawk_barchetta_set_pll_idx(srds_access_t *sa__, uint8_t pll_index)
{
    BCMI_BARCHETTA_SLICE_SLICE1r_t slice1;
    PHYMOD_IF_ERR_RETURN(
        BCMI_BARCHETTA_READ_SLICE_SLICE1r(sa__, &slice1));

    /*  Select pll_base_address */
    if(pll_index == 0) {
        BCMI_BARCHETTA_SLICE_SLICE1r_PLL_MICRO_SELf_SET(slice1, 0);
    } else {
        BCMI_BARCHETTA_SLICE_SLICE1r_PLL_MICRO_SELf_SET(slice1, 1);
    }
    PHYMOD_IF_ERR_RETURN(
        BCMI_BARCHETTA_WRITE_SLICE_SLICE1r(sa__, slice1));

    return(ERR_CODE_NONE);
}
